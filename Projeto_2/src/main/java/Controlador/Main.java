    private void tableToString() {
        for (int row = 0; row < jTable1.getRowCount(); row++) {
            for (int column = 0; column < jTable1.getColumnCount(); column++) {
                System.out.print(jTable1.getValueAt(row, column) + " ");
            }
            System.out.println("");
        }
    }

